export function messagesFor(locale: Locale): Messages {
  if (locale === "ja-JP") return ja;
  return { ...ja, ...partialMessages[locale] };
}

export function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale);
}

export function resolveBrowserLocale(tag: string | undefined): Locale {
  if (!tag) return "ja-JP";
  const normalized = tag.toLowerCase();
  if (normalized.startsWith("ja")) return "ja-JP";
  if (normalized.startsWith("zh")) {
    return normalized.includes("tw") || normalized.includes("hant") || normalized.includes("hk") ? "zh-TW" : "zh-CN";
  }
  if (normalized.startsWith("pt")) return "pt-BR";
  const base = normalized.split("-")[0] as Locale;
  return locales.includes(base) ? base : "ja-JP";
}
